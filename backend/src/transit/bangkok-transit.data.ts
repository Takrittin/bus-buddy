const STOPS_BY_ID = Object.fromEntries(
  BANGKOK_STOPS.map((stopItem) => [stopItem.stopId, stopItem]),
) as Record<string, StopSeed>;

function getStop(stopId: string) {
  const stopItem = STOPS_BY_ID[stopId];

  if (!stopItem) {
    throw new Error(`Stop ${stopId} is not defined in Bangkok transit data.`);
  }

  return stopItem;
}

function polylineFromStops(stopIds: string[]) {
  return stopIds.map((stopId) => getStop(stopId).location);
}

function polylineFromPath(path: Array<string | Location>) {
  return path.map((node) => (typeof node === 'string' ? getStop(node).location : node));
}

function reverseDirection(
  directionId: DirectionId,
  origin: string,
  destination: string,
  directionLabel: string,
  stopIds: string[],
  polyline: Location[],
) {
  return {
    directionId,
    origin,
    destination,
    directionLabel,
    stopIds,
    polyline,
  };
}

function createBidirectionalRoute(config: {
  routeId: string;
  routeNumber: string;
  routeName: string;
  color: string;
  origin: string;
  destination: string;
  outboundDirection: string;
  inboundDirection: string;
  firstBusTime: string;
  lastBusTime: string;
  averageHeadwayMinutes: number;
  baseCruiseSpeedKmh: number;
  maxSpeedKmh: number;
  serviceLevel: RouteSeed['serviceLevel'];
  targetVehiclesPerDirection?: number;
  morningPeakFlow: RouteSeed['morningPeakFlow'];
  eveningPeakFlow: RouteSeed['eveningPeakFlow'];
  outboundStops: string[];
  outboundPath?: Array<string | Location>;
}): RouteSeed {
  const fallbackOutboundPolyline = polylineFromPath(
    config.outboundPath ?? config.outboundStops,
  );
  const inboundStops = [...config.outboundStops].reverse();
  const fallbackInboundPolyline = [...fallbackOutboundPolyline].reverse();
  const generatedRoadPolyline = GENERATED_ROAD_POLYLINES[config.routeId];
  const outboundPolyline =
    generatedRoadPolyline?.outbound?.length >= 2
      ? generatedRoadPolyline.outbound
      : fallbackOutboundPolyline;
  const inboundPolyline =
    generatedRoadPolyline?.inbound?.length >= 2
      ? generatedRoadPolyline.inbound
      : fallbackInboundPolyline;

  return {
    routeId: config.routeId,
    routeNumber: config.routeNumber,
    routeName: config.routeName,
    color: config.color,
    origin: config.origin,
    destination: config.destination,
    outboundDirection: config.outboundDirection,
    inboundDirection: config.inboundDirection,
    firstBusTime: config.firstBusTime,
    lastBusTime: config.lastBusTime,
    averageHeadwayMinutes: config.averageHeadwayMinutes,
    baseCruiseSpeedKmh: config.baseCruiseSpeedKmh,
    maxSpeedKmh: config.maxSpeedKmh,
    serviceLevel: config.serviceLevel,
    targetVehiclesPerDirection: config.targetVehiclesPerDirection,
    morningPeakFlow: config.morningPeakFlow,
    eveningPeakFlow: config.eveningPeakFlow,
    directions: {
      outbound: reverseDirection(
        'outbound',
        config.origin,
        config.destination,
        config.outboundDirection,
        config.outboundStops,
        outboundPolyline,
      ),
      inbound: reverseDirection(
        'inbound',
        config.destination,
        config.origin,
        config.inboundDirection,
        inboundStops,
        inboundPolyline,
      ),
    },
  };
}

