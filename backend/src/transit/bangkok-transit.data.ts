import {
  DirectionId,
  Location,
  RouteSeed,
  StopSeed,
} from './transit.types';
import { GENERATED_ROAD_POLYLINES } from './generated-road-polylines';

function point(lat: number, lng: number): Location {
  return { lat, lng };
}

function stop(
  stopId: string,
  stopName: string,
  lat: number,
  lng: number,
  landmark: string,
  areaDescription: string,
  isMajorStop: boolean,
  isInterchange: boolean,
  zone: StopSeed['zone'],
): StopSeed {
  return {
    stopId,
    stopName,
    location: point(lat, lng),
    landmark,
    areaDescription,
    isMajorStop,
    isInterchange,
    zone,
  };
}

